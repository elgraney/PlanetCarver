public class ColourPalette {
    Color mainLand;
    Color ocean;
    Color lightVariant;
    Color midVariant;
    Color darkVariant;

    public ColourPalette(Color mainLand, Color ocean, Color lightVariant, Color midVariant,Color darkVariant){
        this.mainLand = mainLand;
        this.ocean = ocean;
        this.lightVariant = lightVariant;
        this.midVariant = midVariant;
        this.darkVariant = darkVariant;
    }

}
